    def encoder(self, int16) -> int:
        """Returns compressed 8 bit value from 16 bit value"""
        if int16 >= 2**15:
            int16 = 2**15 -1
        neg = sign(int16) == -1
        if neg:
            int16 = (-1*int16)&0xffff #< If negative, make positive
        if (int16 > self.cClip):
            int16 = self.cClip;
        int16 = (int16 + self.cBias)&0xffff
        exponent = self.CompressTable[ (int16>>7)&0xff ]
        mantissa = (int16 >> (exponent+3))&0xf #< 4 bit mantisa
        int8 = (exponent<<4)|mantissa
        if neg:
            return -int8
        return int8

    def decoder(self, int8) -> int:
        """Returns the decompressed 16 bit value from 8 bit value"""
        return self.DecompressTable[int8 +0x80]

    def encoderSlow(self, int16) -> int:
        x = int16/(2**15)
        return int(128*sign(x)*log(1 +self._u*abs(x))/self._log_u_1)

    def decoderSlow(self, int8) -> int:
        y = int8/(2**7)
        return (2**15)*sign(y)*((1+self._u)**abs(y) -1)/self._u

